#include "3-calc.h"
/**
 * op_add - a function that adds two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: addition of operands
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that substract two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: substraction of operands
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: multiplication of operands
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: division of operands
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that finds the modulo of two operands
 * @a: first operand
 * @b: second operand
 *
 * Return: modulo of operands
 */
int op_mod(int a, int b)
{
	return (a % b);
}
